# 1.Arithmetic Operator
FirstNumber = 5
SecondNumber = 2
print(FirstNumber + SecondNumber)
print(FirstNumber - SecondNumber)
print(FirstNumber * SecondNumber)
print(FirstNumber % SecondNumber)
print(FirstNumber / SecondNumber)
print(FirstNumber ** SecondNumber)
print(FirstNumber // SecondNumber)  # Floor Division

# 2.Assignment Operator
Number = 5
Number -= 5
Number *= 5
Number /= 5
Number %= 5
Number //= 5
Number **= 5

# 3.Comparison Operators
x = 5
y = 2
print(x > y)
print(x < y)
print(x >= 5)
print(x <= 5)
print(x == 5)
print(x != 5)

# 4.Logical Operators
var1 = 'true'
var2 = 'False'
print(var1 and var2)
print(var1 or var2)
print(not var1)
